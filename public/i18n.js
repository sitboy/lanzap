// 语言检测:精确码(zh-TW) → 主码(zh/pt/…) → 英文
(function () {
  const nav = (navigator.language || 'en');
  const low = nav.toLowerCase();
  let lang = 'en';
  if (low.startsWith('zh')) lang = /tw|hk|hant/.test(low) ? 'zh-TW' : 'zh';
  else { const two = low.slice(0, 2); if (I18N[two]) lang = two; }
  window.LANG = localStorage.lang && I18N[localStorage.lang] ? localStorage.lang : lang;
})();
window.t = (k, vars) => {
  let s = (I18N[LANG] && I18N[LANG][k]) || I18N.en[k] || k;
  if (vars) for (const [kk, v] of Object.entries(vars)) s = s.replace('{' + kk + '}', v);
  return s;
};
